// Category: MELTDOWN — dysregulated emotional peak, child has lost control
// Examples: screaming, crying uncontrollably, flailing on floor, hyperventilating,
// "I hate you", throwing themselves down, fight-or-flight activated

export const MELTDOWN_GUIDANCE = `
== CATEGORY: MELTDOWN ==

What's actually happening:
The child's thinking brain is offline. They are not being manipulative. They are not choosing this. Their nervous system has hijacked them. Logic, explanation, and negotiation will not land — there is no one home to receive them.

The parent's job right now is NOT to stop the meltdown. It is to be the steady one. Meltdowns end. The parent just has to outlast it without joining it.

What the parent SHOULD do:
— Get low. Get still. Soften their face.
— Speak less, not more. One sentence. Then silence.
— Name the feeling in 2-4 words. Do not explain. Do not justify. Do not reason.
— Become a safe rock in the storm. Physical presence > words.
— Wait. The wave crests and breaks. You wait.

What the parent should NOT do:
— Do not try to fix it, problem-solve, or offer choices.
— Do not lecture, question, or bargain.
— Do not say "calm down", "stop crying", "use your words", or "you're fine".
— Do not walk away in frustration. Stay, but don't escalate.
— Do not flood them with words. Silence with presence is better than talking.

Script shape for meltdown:
REGULATE — parent's own body drops first. Hands open, shoulders down, face soft.
CONNECT — 2-4 word feeling label. Full stop. No "but". No explanation.
GUIDE — one physical next step. Not a choice. Not a lecture. Something small.

VOICE CHECK: If any part of your script would make a screaming child scream LOUDER, rewrite it. If any part requires the child to think, reason, or decide — rewrite it.

Age calibration for meltdown:
— Age 2-4: 4 words max per script line. "You're so mad." "I'm right here." "It's okay to cry."
— Age 5-9: Slightly longer but still under 10 words. Name the feeling specifically.
— Age 10-12: Respectful. No babyish "big feelings" talk. "That was hard." works.
— Age 13+: Do not crowd them. Presence from across the room may be more than they can tolerate up close. Give space AND stay.

AVOID examples for this category:
— "You need to calm down right now."
— "Stop crying or we're leaving."
— "This is ridiculous."
— "I don't understand why you're so upset."
`;
