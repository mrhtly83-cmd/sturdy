// Examples: hitting parent, hitting sibling, throwing objects, biting, headbutting,
// spitting, damaging property in anger, pushing, kicking walls, self-hitting
// NOTE: This is the highest-stakes category. Safety comes before connection.

export const AGGRESSION_GUIDANCE = `
== CATEGORY: AGGRESSION ==

What's actually happening:
The child is in fight mode. Their nervous system has chosen attack as survival. The aggression is not a choice they're making in their thinking brain — it's a body response that has taken over. This doesn't excuse harm. It explains why "stop it" doesn't work.

The parent has TWO jobs, in this exact order:
1. STOP the harm. Bodies safe first. Feelings second.
2. Once safe, connect AFTER the wave passes. Not during.

This is the ONE category where physical intervention by the parent is not just allowed — it's required. A child hurting someone needs the parent to be bigger than the aggression.

What the parent SHOULD do:
— Move bodies apart IMMEDIATELY. Step between. Scoop up the smaller one. Get to safety.
— Use your body, not your voice, to stop the harm. Hands are faster than words.
— Short, clear commands: "I won't let you hit." "Hands stay here." "Body safe."
— Voice stays LOW and steady. Not yelling. Not pleading. Flat and firm.
— Once physical safety is established, drop to their level and wait for the wave to pass.
— Name what happened AFTER the calm returns, not during. "You were so mad your hands hit."
— Repair matters. Once regulated, help them see the impact — gently.

What the parent should NOT do:
— Do not hit back, shake, or grab harder than necessary to stop the harm.
— Do not yell "STOP!" repeatedly — it becomes white noise and adds chaos.
— Do not lecture mid-aggression. The thinking brain cannot hear you.
— Do not ask "why did you do that?" in the moment. They do not know why.
— Do not shame: "You're a bad kid", "What's wrong with you", "I'm ashamed of you".
— Do not hug a child who is still swinging. Safety first, connection when ready.
— Do not ignore or minimize if a sibling was hurt. Both children need care.

Script shape for aggression:
REGULATE — parent's body becomes the safety. Physical intervention described concretely.
CONNECT — short command that holds the line. "I won't let you hurt [person]." No reasoning yet.
GUIDE — the physical next step once bodies are separated. Move them. Hold them. Space them.

VOICE CHECK: The script must name HANDS, BODY, or PHYSICAL action in every step. Aggression moments require physical response before words. If your script is three sentences of talk, rewrite it — the parent needs to MOVE first.

Age calibration for aggression:
— Age 2-4: Physical intervention is primary. Words are minimal. "No hitting." while moving them.
— Age 5-9: Hold their hands gently but firmly. "I won't let you hit. Hands safe with me."
— Age 10-12: Cannot physically restrain easily. Create distance. Remove the target. "We are separating. You are not in trouble. We need space."
— Age 13+: Cannot physically contain a teen. Step back. Do not engage physically. If truly unsafe, leave the room and get help. Safety first, even if that means stepping away.

SPECIAL NOTES:
— If the aggression is toward a sibling or other child: the other child comes FIRST. They are the victim in this moment and need safety + acknowledgment.
— If the aggression is toward the parent: protect yourself. You cannot parent if you are hurt.
— If the aggression is self-directed (hitting self, head-banging): gently restrain if age allows. Whisper close: "I'm here. I won't let you hurt you."
— If there's a weapon or serious danger: this is no longer a parenting moment. Get out, call for help.

AVOID examples for this category:
— "What is wrong with you?!"
— "I can't believe you did that."
— "You're grounded for a week."
— "Apologize to your sister right now."
— "We don't hit in this house." (they already know)
— "You're just like [other parent] when you get angry."
`;
