// supabase/functions/_shared/prompts/categories/transition.ts
// Category: TRANSITION — changing activities, leaving, arriving, switching tasks
// Examples: leaving the park, leaving grandma's house, getting out of the bath,
// turning off the screen, leaving a friend's house, getting in the car, coming
// to the dinner table, stopping play to do homework, going to bed
// NOTE: Transitions are the #1 most common parenting friction point. Brains
// don't switch tracks easily — especially young brains and neurodivergent brains.

export const TRANSITION_GUIDANCE = `
== CATEGORY: TRANSITION ==

What's actually happening:
The child is being asked to leave one brain state and enter another. They are happy, engaged, absorbed, or simply not ready to stop. Their brain is built to finish what it starts — and the parent is asking them to break that loop.

This is NOT defiance. It is not a character flaw. It is how child brains work. Younger children and neurodivergent children feel transitions in their body like a physical interruption.

The parent's job is NOT to enforce the switch. It is to BRIDGE between the two states. Transitions ruined by force create meltdowns. Transitions bridged with signal, ritual, and acknowledgment flow peacefully.

The secret: transitions go better BEFORE they begin. The script is often about the 2-5 minutes before the switch, not the moment of the switch itself.

What the parent SHOULD do:
— Give a heads-up. "Five more minutes." Then "Two more minutes." Not the first time they've heard it.
— Narrate what you see them doing — it signals you see them, not just the clock.
— Name the next thing concretely, not abstractly. "We're going home for dinner" not "We have to leave now."
— Offer a bridge object or ritual. A goodbye wave. A specific song on the way. Carrying a rock from the park.
— Move yourself first. Start gathering your things. Walking out ahead. Modeling the transition.
— If the wave starts, meet it with compassion: "It's hard to leave when you're having fun."
— For tough transitions, build in CONSISTENT rituals. Same song every car ride. Same three-step bedtime. Predictability is kindness.

What the parent should NOT do:
— Do not ambush. Do not say "time to go" out of nowhere.
— Do not use the word "now" as the first word of a transition. "Now we're leaving" triggers refusal.
— Do not rush or pull them physically without warning.
— Do not bribe. "If you come now you get candy" teaches them to negotiate the next time.
— Do not shame: "You're always like this" or "Other kids don't do this".
— Do not add urgency they can't understand. "I'm going to be late for work" is not a child's problem.

Script shape for transition:
REGULATE — parent gets calm BEFORE the transition starts. Most transitions fail because the parent is already frustrated.
CONNECT — name what the child is doing, acknowledge the hard part of stopping. "You're in the middle of a great tower."
GUIDE — the bridge. A physical ritual, a countdown, a carry-object, a binary choice that moves the body.

VOICE CHECK: Does your guide step give the child a SPECIFIC physical action to take, or a ritual to follow? Abstract "let's go" scripts fail. Scripts must give the body somewhere to go.

Age calibration for transition:
— Age 2-4: Visual/physical cues work better than verbal. "The park is going to sleep. Wave bye-bye." Pick them up tenderly, not suddenly.
— Age 5-9: Offer countdown + choice. "Two more minutes. Then you can walk to the car or hop like a bunny."
— Age 10-12: Respect the flow state. Give more heads-up time. "In five minutes we need to head out — find a good stopping point."
— Age 13+: Almost always about autonomy. Consult, don't command. "I need to leave by 4. Let me know what you need to wrap up." Give them the dignity of planning their own exit.

NEURODIVERGENT NOTE (applies silently if detected):
— ADHD: Transitions from high-dopamine activities are especially hard. More warnings, more concrete bridges.
— Autism: Changes in expected routine cause real distress. Name what's changing and why, literally and calmly.
— PDA: Never give a direct command. "The car is leaving at 4 — anyone who wants a ride should be in it" works better than "get in the car".

AVOID examples for this category:
— "I said NOW!"
— "You have until the count of three."
— "Fine, stay here by yourself then."
— "You're ruining our whole day."
— "Other kids listen the first time."
`;
