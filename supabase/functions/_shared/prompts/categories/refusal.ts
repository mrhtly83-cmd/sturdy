// Category: REFUSAL — child is dug in, won't comply, "no"
// Examples: won't put shoes on, won't leave, won't eat, won't get in the car,
// won't do homework, crosses arms, plants feet, flat refusal without big emotion

export const REFUSAL_GUIDANCE = `
== CATEGORY: REFUSAL ==

What's actually happening:
The child is NOT dysregulated. They are not in a meltdown. They have their thinking brain online — and they are using it to say no. This is often about autonomy, power, or having missed a transition cue. Sometimes it's sensory (the shoes feel wrong). Sometimes it's emotional fuel tank empty. Sometimes it's a real "no" that deserves respect.

The parent's job is NOT to win. It is to hold the limit without making it a fight. Every word added gives the child more to push against. The shorter the script, the stronger the limit.

What the parent SHOULD do:
— State the thing once. Calmly. Clearly. No preamble.
— Offer a genuine binary choice if one exists. Two options you can live with.
— Then wait. Do not repeat. Do not explain. Do not negotiate.
— Keep your body relaxed. Unclench your jaw. Drop your shoulders.
— Use when-then language: "When your shoes are on, we can go to the car."
— If appropriate, physically move toward the action yourself — parents model the transition.

What the parent should NOT do:
— Do not ask a yes/no question. "Can you put your shoes on?" invites "no".
— Do not repeat yourself more than once. Repetition = weakness in a refusal moment.
— Do not explain why. Children in refusal mode are not persuaded by reasons.
— Do not countdown, threaten, or bribe — it teaches children these tactics for next time.
— Do not match their energy. If they're dug in, you get calmer, not more intense.

Script shape for refusal:
REGULATE — parent body drops into calm. Not rushed. Not frustrated.
CONNECT — acknowledge the "no" without fighting it. One sentence. Validate + hold.
GUIDE — the choice OR the when-then OR the physical next step. Short and concrete.

VOICE CHECK: Read the guide step aloud. Does it end with "okay?" or a question mark? If yes, rewrite as a statement. Refusal scripts must be declarative.

Age calibration for refusal:
— Age 2-4: Two physical options. "Walking shoes or carried shoes — which one?"
— Age 5-9: When-then format lands well. "When your homework is done, we can ride bikes."
— Age 10-12: Respect autonomy. Explain the non-negotiable briefly, then hold. No lectures.
— Age 13+: Collaborative language. "I hear you. And this still needs to happen today." Give them the autonomy of HOW, not WHETHER.

AVOID examples for this category:
— "How many times do I have to ask you?"
— "Because I said so."
— "Fine, don't do it. See what happens."
— "Do you want me to count to three?"
— "You're being so difficult right now."
`;
