// Category: SHUTDOWN — child has gone quiet, withdrawn, stopped responding
// Examples: silent treatment, won't make eye contact, won't come out of room,
// curled up facing away, one-word answers, "leave me alone", blank stare,
// teen slamming door and refusing to talk, younger child hiding under blanket

export const SHUTDOWN_GUIDANCE = `
== CATEGORY: SHUTDOWN ==

What's actually happening:
The child has moved past meltdown into shutdown. The nervous system has flipped from fight-or-flight into freeze. They are not ignoring the parent. They are not being dramatic. Their system has gone underground to protect itself because the flooding became too much to tolerate.

This is the HARDEST category to script for, because the parent's instinct — to reach, to reconnect, to draw them out — is usually the exact wrong move. Pressure deepens shutdown. Space restores safety.

The parent's job is to BECOME SAFE TO APPROACH. Not to pull them out. Not to get them to talk. Just to be a warm, patient, non-demanding presence that the child can re-enter when they're ready.

What the parent SHOULD do:
— Reduce demands to zero. No questions. No requests. No eye contact pressure.
— Lower their voice. Slow their body. Move like you're near a sleeping animal.
— State presence, not invitation. "I'm here" not "Come talk to me."
— Offer a side-by-side object: a water bottle slid across the floor, a blanket placed nearby, a snack left on the desk.
— Give permission to stay shut down. "You don't have to talk right now."
— Wait. The opening comes. It always comes. You just cannot rush it.

What the parent should NOT do:
— Do not ask "What's wrong?" or "Do you want to talk?"
— Do not chase, pursue, or stand over them.
— Do not force eye contact or physical touch.
— Do not say "I'm trying to help you" — it puts the burden on them.
— Do not interpret silence as disrespect and escalate consequences.
— Do not make it about you: "You're hurting my feelings" adds weight to a child who is already overloaded.

Script shape for shutdown:
REGULATE — parent slows everything. Sit near, not across from. Soften shoulders.
CONNECT — name the state without demanding a response. "I can tell you're done." Then stop.
GUIDE — a gesture of presence. A tangible, non-verbal offering. Or simply: "I'll be nearby."

VOICE CHECK: If your script expects the child to speak, move, or respond — rewrite it. The entire script should work even if the child does nothing.

Age calibration for shutdown:
— Age 2-4: Rare at this age, often looks like dissociation. Scoop them close silently. No words needed.
— Age 5-9: Sit 3-6 feet away. Offer comfort object without asking. "Benny's here if you want him."
— Age 10-12: Respect the door. Speak from the other side. "I'll be downstairs. There's water here."
— Age 13+: Maximum space. A note slid under the door can land better than a voice through it. "No rush. I'm here when you're ready. No lecture coming."

AVOID examples for this category:
— "We need to talk about this now."
— "You can't just shut me out."
— "Why aren't you answering me?"
— "This is exactly what you always do."
— "I'm going to count to three and then you're coming out."
`;
